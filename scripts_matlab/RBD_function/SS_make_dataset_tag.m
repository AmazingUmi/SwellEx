function dataset_tag = SS_make_dataset_tag(split_strategy, split_metadata, ...
    segment_start_s, segment_end_s, segment_step_s)
%SS_MAKE_DATASET_TAG Build a filename tag that matches split semantics.
switch string(split_strategy)
    case "periodic"
        dataset_tag = SS_make_time_tag(segment_start_s, segment_end_s, ...
            segment_step_s);
    case "Range_nearby"
        selected_start_s = min([split_metadata.left_time_start_s, ...
            split_metadata.right_time_start_s]);
        selected_end_s = max([split_metadata.left_time_end_s, ...
            split_metadata.right_time_end_s]);
        dataset_tag = sprintf( ...
            'selected_s%d_to_s%d_min_s%d_step_s%d', ...
            round(selected_start_s), round(selected_end_s), ...
            round(split_metadata.range_min_time_s), round(segment_step_s));
    otherwise
        dataset_tag = SS_make_time_tag(segment_start_s, segment_end_s, ...
            segment_step_s);
end
end
