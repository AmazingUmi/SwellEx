function dataset_variant_tag = ELM_make_dataset_variant_tag( ...
    frequency_selection_modes, frequency_config, segment_duration_s, ...
    num_snapshots_per_segment, snapshot_overlap_count)
%ELM_MAKE_DATASET_VARIANT_TAG Build a stable ELM dataset variant tag.
% Example:
%   elm_pairwise_lsr_upper_mel64_snap1s_ns4_ov3

frequency_tag = DS_make_frequency_selection_tag( ...
    frequency_selection_modes, frequency_config);
snapshot_tag = DS_format_seconds_for_tag(segment_duration_s);

dataset_variant_tag = sprintf( ...
    'elm_pairwise_lsr_upper_%s_snap%ss_ns%d_ov%d', ...
    frequency_tag, snapshot_tag, ...
    round(num_snapshots_per_segment), round(snapshot_overlap_count));
dataset_variant_tag = DS_sanitize_dataset_variant_tag(dataset_variant_tag);
end
